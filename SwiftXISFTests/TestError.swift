import Foundation

/// Generic failure used by the test helpers for their own preconditions.
///
/// Test-only on purpose: the library's public ``XISFError`` must contain only
/// errors the library itself emits, so the test utilities raise this instead
/// when one of their own preconditions is not met.
enum TestError: Error
{
    /// A test helper precondition was violated.
    ///
    /// - Parameter reason: A human-readable explanation of the failure.
    case invalid( reason: String )
}
